package gynfm.common.service.model;

public class PersonModel {
	private int id;
	private String name;
	private String img;
	private String introdction;
	private String description;
	private Integer type;
	private Integer order;
	private Integer is_tutor;
	private String tutor_info;
	// Constructors

	/** default constructor */
	public PersonModel() {
	}

	public PersonModel(int id, String name, String img, String introdction,
			String description, Integer type,Integer order,Integer is_tutor,String tutor_info) {
		super();
		this.id = id;
		this.name = name;
		this.img = img;
		this.introdction = introdction;
		this.description = description;
		this.type = type;
		this.is_tutor = is_tutor;
		this.tutor_info = tutor_info;
		this.order = order;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getIntrodction() {
		return introdction;
	}

	public void setIntrodction(String introdction) {
		this.introdction = introdction;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getOrder() {
		return order;
	}

	public void setOrder(Integer order) {
		this.order = order;
	}
	public Integer getIs_tutor() {
		return this.is_tutor;
	}

	public void setIs_tutor(Integer is_tutor) {
		this.is_tutor = is_tutor;
	}
	
	public String getTutor_info() {
		return this.tutor_info;
	}

	public void setTutor_info(String tutor_info) {
		this.tutor_info = tutor_info;
	}
	@Override
	public String toString() {
		return "PersonModel [id=" + id + ", name=" + name + ", img=" + img
				+ ", introdction=" + introdction + ", description="
				+ description + ", type=" + type + "]";
	}
	
}
